<?php

//  Operadores de incremento e decremento
// Servem para somar ou subtrair em (+1 ou -1)
// qualquer valor do tipo numérico ou string.

// Incremento
$valor = 20;

//Pré incremento
echo ++$valor;
echo "<hr>";

// Pós-incremento
echo $valor++;
echo "<br>";
echo $valor;
echo "<hr>";

////////////////////////

//Decremento

//Pré-decremento
echo --$valor;
echo "<hr>";

//Pós-drecremento
echo $valor--;
echo "<br>";
echo $valor;



?>